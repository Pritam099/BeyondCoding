vector<int> find(int arr[], int n , int x )
{
    // code here
    vector<int> ans{-1,-1};
    for (int i = 0; i < n; i++)
    {
        if(arr[i]==x){
            ans.clear();
            ans.push_back(i);
            while(arr[i]==x){
                i++;
            }
            ans.push_back(i-1);
            return ans;
        }
    }
    
    return ans;
}

